package com.basicjava;

public class SwitchDemo3 {

    public static void main(String[] args) {

        String name = "habib";

        switch (name) {
            case "habib":
                System.out.println("habib uddin");
                break;
            case "imran":
                System.out.println("imran hussain");
                break;
            case "shorif":
                System.out.println("shorif uddin");
                break;
            default:
                System.out.println("none");


        }


    }


}

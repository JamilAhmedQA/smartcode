package com.basicjava;

public class SwitchDemo {

    public static void main(String[] args) {

        String name = "Jamil";

        switch (name) {

            case "Jamil":
                System.out.println("nice guy");
                break;
            case "Aman":
                System.out.println("smart");
                break;
            case "Taher":
                System.out.println("cool");
                break;
            default:
                System.out.println("nothing");


        }


    }

}

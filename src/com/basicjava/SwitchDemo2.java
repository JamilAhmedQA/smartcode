package com.basicjava;

public class SwitchDemo2 {

    public static void main(String[] args) {

          int age = 35;

          switch (age){
              case 35:
                  System.out.println(10);
                  break;
              case 50:
                  System.out.println(20);
                  break;
              case 80:
                  System.out.println(15);
                  break;
              default:
                  System.out.println(00);
                  break;





          }








    }
}
